        <p align="left"><b>WHAT ARE DENTAL IMPLANTS?</b></p>
        <p align="left">A dental implant is a small post, usually made of titanium, that serves as a substitute for the root of the tooth. A connector, known as an abutment, is placed on, or built into, the top of the dental implant, which connects it to the replacement tooth. Finally, a crown, a replacement tooth, is custom fabricated to match your natural teeth, completing the dental implant.
        
        <p align="left"><b>WHO IS A GOOD CANDIDATE FOR DENTAL IMPLANTS?</b></p>
        <p align="left">Almost anyone who is missing a single tooth, multiple teeth, or all teeth are candidates for dental implants. You must have a strong, healthy jawbone and gums in order for dental implants to be a viable tooth replacement option for you. If you have experienced bone loss due to a tooth abscess, or suffer from oral health problems such as gingivitis, dental implants may not be for you.
    
        <p align="left"><b>HOW LONG DOES IT TAKE TO REPLACE TEETH USING DENTAL IMPLANTS?</b></p>
        <p align="left">Each patient is different. After completing the necessary diagnostics, your implant dentist should provide you with a treatment plan including the estimated amount of time to complete the process. Total treatment time depends on your needs, medical and dental history, the health of the supporting bone in yourjaw, and the technique and materials that will be used. It can be as short as a few months, or more than a year for more complex conditions.
      
        <p align="left"><b>DOES IT HURT TO GET DENTAL IMPLANTS? </b></p>
        <p align="left">Humans are "blessed" with 2 sets of teeth (baby teeth and adult/permanent teeth). When a single permanent tooth or multiple teeth are lost due to dental decay or gum disease, dental implants can now replace them as the 3rd set of fixed teeth! Dental implants can last a lifetime and can improve your appearance, your confidence, and your ability to eat the foods you like, and participate in an active lifestyle, without worrying about your teeth. Dental implants are made of titanium and can never get dental decay!
    
