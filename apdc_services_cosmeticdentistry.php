        <p align="left"><b>WHAT IS COSMETIC DENTISTRY?</b></p>
        <p align="left">Cosmetic dentistry is any dental work that improves the appearance of teeth, gums and/or bite. It primarily focuses on improvement dental aesthetics in color, position, shape, size, alignment and overall smile appearance. Starting from minor changes to major oral surgery, there is a range of cosmetic dentistry techniques that can treat dental flaws and fix discoloured, chipped, or missing. Such flaws dent confidence of many, and if you are one of them it's time to fix them.
            
        <p align="left"><b>I DON'T LIKE MY TEETH OR MY SMILE - WHAT CAN I DO? </b></p>
        <p align="left">A wide variety of options are available to improve tooth function and how your smile looks.

        <p align="left"><b>WHAT ARE TYPES OF COSMETIC DENTISTRY PROCEDURES?</b></p>

        <p align="left"><b>1. COMPOSITE BONDING</b></p>
        <p align="left">Composite bonding repairs damaged, decayed, or discoloured teeth using material that matches the colour of tooth enamel. First, your dentist will drill out the area infected by tooth decay and apply composite on the surface, and then shapes and polishes it up just right to match the other

        <p align="left"><b>2. DENTAL VENEERS</b></p>
        <p align="left">Sometimes called porcelain veneers or dental porcelain laminates, veneers are wafer-thin, custom-made shells of tooth-colored materials designed to cover the front surface of teeth to improve your appearance. These shells are bonded to the front of the teeth changing their color, shape, size, or length.
        
        <p align="left"><b>3. INLAYS AND ONLAYS</b></p>
        <p align="left">These types of procedures are also called "Indirect Fillings." Dentists apply these when a tooth has moderate decay or there is not enough tooth structure in your mouth to support a filling.
            
        <p align="left"><b>4. DENTURES</b></p>
        <p align="left">There are typically two types of dentures: complete dentures and partial dentures. The purpose of using dentures is to protect patients from the negative effects of tooth loss.

        <p align="left"><b>5. DENTAL IMPLANTS</b></p>
        <p align="left">Effective for replacing damaged teeth or lost tooth. During the process, the dentist inserts a tiny piece of titanium and fixes into yourjaw where a tooth is missing that supports a crown.

        <p align="left"><b>6. TEETH WHITENING</b></p>
        <p align="left">Over the years, your teeth may get stained, worn, and yellowish from food, drinks, medication and also due to personal habits like smoking. Whitening removes these stains and provides a shiny cover to your teeth.

        <p align="left"><b>8. BRACES</b></p>
        <p align="left">These are orthodontic tools that your dentist will use to align your teeth properly. In fact your cosmetic dentist may even recommend you braces over other cosmetic procedures because having misaligned teeth may cause several dental health issues.
        
        <p align="left"><b>9. GINGIVOPLASTY</b></p>
        <p align="left">It's the surgical reshaping of gum tissue around the teeth. It is often is done simply to make gums look better. Also use for gummy smile patients.

        <p align="left"><b>10. COSMETIC TEETH SHAPINGS</b></p>
        <p align="left">Also referred to as "enamel shaping, " the dentist can reshape the tooth by filing or removing some of the enamel. This process is usually not painful and can produce immediate effects. Consult your dentist at Fashion Smile to discuss what you would like to have changed and which procedure is best suited for your needs.

            
